int	ft_is_prime(int nb)
{
	int	root;

	root = 2;
	if (nb == 1 || nb <= 0)
		return (0);
	else if (nb == 2 || nb == 3)
		return (1);
	while (root * root <= nb)
	{
		if (nb % root == 0)
			return (0);
		++root;
	}
	return (1);
}
